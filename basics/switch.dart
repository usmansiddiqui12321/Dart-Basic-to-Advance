void main(){
  String color = 'red';

// Check the value of color using a switch statement
switch (color) {
  case 'red':
    print('The color is red.');
    break;
  case 'blue':
    print('The color is blue.');
    break;
  case 'green':
    print('The color is green.');
    break;
  default:
    print('The color is not recognized.');
}

}